const os = require('os')

console.log(os.platform()) //current platform
console.log(os.arch()) //processor architecture
console.log(os.cpus()) // cpus information
console.log(os.freemem()) //free memory
console.log(os.totalmem()) // all memory
console.log(os.homedir()) // home directory
console.log(os.uptime()) //uptime in milliseconds
